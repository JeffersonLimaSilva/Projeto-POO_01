    public void cadastrar(){
        n_metalico=  new MineralNaoMetalico();
        
        n_metalico.setCodIdentificador(Integer.parseInt(cxCodIdentificador.getText()));
        n_metalico.setNome(cxNome.getText());
        n_metalico.setCor(cxCor.getText());
        try{
                n_metalico.setComposicaoQuimica(cxCompQuimica.getText());
                n_metalico.setDureza(Integer.parseInt(cxDureza.getText()));
                n_metalico.setPeso(Float.parseFloat(cxPeso.getText()));
                n_metalico.setValorComercial(Double.parseDouble(cxValorComercial.getText()));
        }
        catch(ComposicaoQGrdException cge){
                cge.impCQGrd();
                
        }
        catch(DurezaPqnException dpe){
                dpe.impDPqn();
                
        }
        catch(DurezaGrdException dge){
                dge.impDGrd();
                cxCompQuimica.setText("");
                cxCompQuimica.requestFocus();
                
        }
        catch(NumberFormatException nfe){
            JOptionPane.showMessageDialog(null, "Formato Errado Informado", "Cadastro Mineral Precioso", 0);
             
        }
        n_metalico.setTransparencia(cxTransparencia.getText());
        n_metalico.setIsolamento(cxIsolamento.getText());
        n_metalico.getOrigem().setPais(cxPais.getText());
        n_metalico.getOrigem().setCidade(cxCidade.getText());
        n_metalico.getOrigem().setTipo_Extracao(cxTipExtracao.getText());
        
        n_metalico=bdMNMet.cadMNMetalico(n_metalico);
        if(n_metalico != null){
            JOptionPane.showMessageDialog(null, "Casdastro Realizado", "Cadastro Mineral Precioso", 1);
            limpar();
        }
        else{
            JOptionPane.showMessageDialog(null, "Mineral com esse Codigo, ja existe", "Cadastro Mineral Precioso", 0);
        }
    }
    public void sair(){
        int resp = JOptionPane.showConfirmDialog(null, "Deseje Sair","Sair", JOptionPane.YES_NO_CANCEL_OPTION);
        if(resp == 0){
            this.dispose();
        }
    }
  
