    public void excluirCod(){
        n_metalico= new MineralNaoMetalico();
        try{
        n_metalico.setCodIdentificador(Integer.parseInt(cxCodIdentificador.getText()));
        n_metalico=bdMNMet.remvMNMetalico(n_metalico);
        }
        catch(NumberFormatException nfe){
            JOptionPane.showMessageDialog(null, "Formato Errado Informado", "Cadastro Mineral Precioso", 0);    
        }
    }
    public void alterar(){
        n_metalico = new MineralNaoMetalico();
        
        try{
            n_metalico.setCodIdentificador(Integer.parseInt(cxCodIdentificador.getText()));
            n_metalico=bdMNMet.atualN_metalico(n_metalico);
            if(n_metalico != null){
                cxCodIdentificador.setText(Integer.toString(n_metalico.getCodIdentificador()));
                cxNome.setText(n_metalico.getNome());
                JOptionPane.showMessageDialog(null, "Dados atualizados com sucesso", "Atualizar Dados", 1);
                
            }
        }
        catch(NumberFormatException nfe){
            JOptionPane.showMessageDialog(null, "Formato Errado Informado", "Cadastro Mineral Precioso", 0);    
        }
    }
    public void listarTab(){
        DefaultTableModel modelo = (DefaultTableModel) jTableNMetalico.getModel();
        
        int posLin=0;
        
        modelo.setRowCount(posLin);
        
        for(MineralNaoMetalico n_metalico : bdMNMet.getBdMNMeta()){
            modelo.insertRow(posLin, new Object[]{
                n_metalico.getCodIdentificador(),
                n_metalico.getNome(),
                n_metalico.getCor(),
                n_metalico.getComposicaoQuimica(),
                n_metalico.getDureza(),
                n_metalico.getPeso(),
                n_metalico.getValorComercial(),
                n_metalico.getTransparencia(),
                n_metalico.getIsolamento(),
                n_metalico.getOrigem().getPais(),
                n_metalico.getOrigem().getCidade(),
                n_metalico.getOrigem().getTipo_Extracao(),
            });
            posLin++;
        }
    }
